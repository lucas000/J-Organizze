    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
       if( (txtAltura.getText().isEmpty()) || (txtPeso.getText().isEmpty()) )
        {
            JOptionPane.showMessageDialog(null, "Por favor preecha todos os campos!");
        }
        else if ( Double.parseDouble(txtAltura.getText())  > 4 || Double.parseDouble(txtAltura.getText()) < 0.1 )
        {
            JOptionPane.showMessageDialog(null, "Altura Inválida");
        }
        else if( Double.parseDouble(txtPeso.getText()) > 300 ||  Double.parseDouble(txtPeso.getText()) < 0)
        {
            JOptionPane.showMessageDialog(null, "Peso Inválido");
        }
        else
        {
            Saude saude = new Saude();
            saude.setAltura(Double.parseDouble(txtAltura.getText()));
            saude.setPeso(Double.parseDouble(txtPeso.getText()));
            saude.setImc(saude.getPeso() / (Math.pow(saude.getAltura(), 2)));
            saude.setSituacao(labelSituacao.getText());

            SaudeDAO dao = new SaudeDAO();
            dao.adicionarSaude(saude);

            dao.getSaude();
            dispose();
        }
    }//GEN-LAST:event_btnRegistrarActionPerformed

    private void txtAlturaKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtAlturaKeyPressed
        if (evt.getKeyCode() == KeyEvent.VK_PERIOD) 
    {  
        pontoAltura++;
        if(pontoAltura > 1)
        {
        JOptionPane.showMessageDialog(null, "Sua altura não pode conter dois pontos.");
        pontoAltura = 0;
        
        String caracteres="098765432.1";
        if(!caracteres.contains(evt.getKeyChar()+""))
        {
        evt.consume();
        }
         
        txtAltura.setText("");
        }
    } 
    }//GEN-LAST:event_txtAlturaKeyPressed

