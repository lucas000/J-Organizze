    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // ESTE CÓDIGO AQUI CALCULA O IMC
        pontoAltura = 0;
        pontoPeso = 0;
        if(txtAltura.getText().isEmpty() || txtPeso.getText().isEmpty())
        {       
            JOptionPane.showMessageDialog(null, "Informe os valores a serem calculados!");
        }else
        {
        btnRegistrar.setEnabled(true);
        MinhAltura = Float.valueOf(txtAltura.getText());
        MePeso = Float.valueOf(txtPeso.getText());
        Resultado = MePeso / (MinhAltura * MinhAltura);
        labelIMC.setText(String.valueOf(Resultado));

        if (Resultado <= 18.5){
            labelSituacao.setText(String.valueOf("ABAIXO DO PESO"));
        }
        if (Resultado > 30 && Resultado <=34.9 ){
            labelSituacao.setText(String.valueOf("OBESIDADE TIPO 1"));
        }
        if (Resultado > 18.6 && Resultado <=24.9 ){
            labelSituacao.setText(String.valueOf("PESO NORMAL"));
        }
        if (Resultado > 25 && Resultado <=29.9 ){
            labelSituacao.setText(String.valueOf("SOBRE PESO"));
        }
        if (Resultado > 30 && Resultado <=34.9 ){
            labelSituacao.setText(String.valueOf("OBESIDADE TIPO 1"));
        }
        if (Resultado > 35 && Resultado <=39.9 ){
            labelSituacao.setText(String.valueOf("OBESIDADE TIPO 2"));
        }
        if (Resultado > 40){
            labelSituacao.setText(String.valueOf("OBESIDADE MÓRBIDA"));
        }
        }

    }//GEN-LAST:event_jButton1ActionPerformed

