    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       
    if(txtCategoria.getText().equals(""))
    {
        JOptionPane.showMessageDialog(null, "Campo vazio");
    }
    else
    {
    try
        {
            instrucao = conexao.prepareStatement("INSERT INTO categoria (categoria,categoria_usuario) values ('" + txtCategoria.getText() + "',?)");//isso so funciona pra varchar
            instrucao.setInt(1, Usuario.getId());
            instrucao.executeUpdate();
            
            
            JOptionPane.showMessageDialog(null, "Nova categoria adicionada");
            AdicionarAtividade.cbCategoria.removeAllItems();
            AdicionarAtividade.populaComboBox();
            dispose();
        }
        catch( SQLException e )
        {
            JOptionPane.showMessageDialog(null, "Categoria j√° existente.");
        }
    }
    }//GEN-LAST:event_jButton1ActionPerformed

